package com.globalsight.selenium.pages;

/**
 * @author Vincent
 * 
 */
public class AsiaOnlineMT
{
    private String host;
    private String port;
    private String username;
    private String password;
    private String accountNumber;
    private String domainCombination;

    public String getHost()
    {
        return host;
    }

    public void setHost(String host)
    {
        this.host = host;
    }

    public String getPort()
    {
        return port;
    }

    public void setPort(String port)
    {
        this.port = port;
    }

    public String getUsername()
    {
        return username;
    }

    public void setUsername(String username)
    {
        this.username = username;
    }

    public String getPassword()
    {
        return password;
    }

    public void setPassword(String password)
    {
        this.password = password;
    }

    public String getAccountNumber()
    {
        return accountNumber;
    }

    public void setAccountNumber(String accountNumber)
    {
        this.accountNumber = accountNumber;
    }

    public String getDomainCombination()
    {
        return domainCombination;
    }

    public void setDomainCombination(String domainCombination)
    {
        this.domainCombination = domainCombination;
    }

}
