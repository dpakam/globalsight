DROP TRIGGER IF EXISTS MODIFIED_ON_TIME;
DELIMITER //
CREATE TRIGGER MODIFIED_ON_TIME BEFORE INSERT ON TB_TERM 
FOR EACH ROW 
BEGIN
    DECLARE T TIMESTAMP DEFAULT '2037-12-31 23:59:59';
    IF NEW.MODIFIED_ON = T
	  THEN
	    SET NEW.MODIFIED_ON = NOW();
	END IF;
END;
//
DELIMITER ;
	

